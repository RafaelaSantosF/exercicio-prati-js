//Implemente um programa que recebe uma nota de 0 a 10 e classifica como "Aprovado", "Recuperação", ou "Reprovado" utilizando if-else if.


let nota = prompt("Digite sua nota:")


if (nota > 6){
    alert("Aprovado")
} else if (nota <= 6 && nota >= 4){
    alert("Recuperação")
}else {
    alert("Reprovado")
}












